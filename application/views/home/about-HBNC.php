                        <h3>About the Programme</h3> 
                        <h5>Home Based New Born Care (HBNC)</h5>
                        <p>Under National Rural Health Mission, Home Based New Born Care is being implemented since 2011 for reduction of neonatal mortality in rural areas. The guidelines on Home Based Newborn Care were revised in 2014. Home Based Newborn Care scheme for reduction of neonatal mortality, has incentivized Accredited Social Health Activist (ASHA) for making visits to all newborns and their mothers according to specified schedule up to 42 days of life. The incentive amounts to a total of Rs. 250 for six visits in case of institutional delivery and seven visits in case of home delivery, subject to the following:</p>
                        <ul>
                            <li>a)	recording of weight of the newborn in Mother Child Protection (MCP) card </li>
                            <li>b)	ensuring BCG , 1st dose of OPV and  DPT vaccination </li>
                            <li>c)	both the mother and the newborn are safe till 42 days of the delivery, and</li>
                            <li>d)	registration of birth has been done</li>
                        </ul>

                        <p>This will be confirmed through recording in MCP cards & ASHA visit form. Special training is being provided to ASHA on Module 6 & 7 in this regards and a kit consisting of required equipment and medicine is also being provided. Currently, Home Based New Born Care through ASHAs is being implemented across the country except Goa and Lakshadweep. More than 9 lakh have provided home visits to 1.3 crore newborns during the financial year 2018-19. </p>

                        <p>Further, provision for extra care to all newborn discharged after treatment of sickness from Special Newborn Care Units (SCNU) and those who are born as preterm or low birth weight babies is being ensured through structured home visits follow up by ASHA till 1 year of life. In case of SNCU discharged newborns, the day of discharge is to be taken as day one. ASHAs would make the first home visit within 24 hours of discharge (Day 1) and complete the remaining home visits as per HBNC visit schedule i.e. 3, 7, 14, 21, 28 and 42nd day from the day of discharge. On completion of these visits ASHA will conduct follow up visit once every quarter starting from 3rdmonth onwards till one year of life i.e., four visits at the completion of 3rd, 6th, 9th and 12th month of life. In case of LBW or preterm newborn who did not require SNCU admission, after completion of HBNC visits (till 42 days of birth), ASHA will visit once every quarter starting from 3rd month onwards till one year of life i.e., four visits at the completion of 3rd, 6th, 9th and 12th month of life. The schedule of the visits has been structured to one visit per quarter linked with an incentive of Rs. 50 per visit per quarter, from the 3rd month until 1 year of age. The incentive is being paid after completion of the age appropriate quarterly visits with completed forms and fulfilling the conditionalities irrespective of the number of visits.</p>


