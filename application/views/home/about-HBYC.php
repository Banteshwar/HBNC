                        <h3>About the Programme</h3> 

                        <h5>Home Based Care for Young Children (HBYC)</h5>

                        <p>Under National Health Mission, Home-Based Care for Young Child Programme (HBYC) is rolled out as an extension of the Home Based New Born Care programme (HBNC) which is currently implemented across the country. Under HBNC, home visits by ASHAs to the infants ends at 42nd day after birth and there existed a gap in the household contact of ASHAs with the child beyond this period except the ones for immunization. Realizing the importance of interventions for reducing diarrhoea, pneumonia, and under nutrition including the role of WASH on overall child survival and development, addressing this gap in health system contact was crucial. Therefore, additional home visits by ASHAs between 3rd and 15th months were proposed under HBYC to fill this gap. </p>

                        <p>ASHA will provide home visits on 3rd, 6th, 9th, 12th and 15th months to promote early initiation of breast feeding, exclusive breast feeding till 6 months and continued breast feeding till 2nd year of life along with adequate complementary feeding and to ensure age appropriate immunization and early childhood development. The quarterly home visits for low birth weight and SNCU discharged babies by ASHAs will be subsumed with HBYC visits in those districts where it is rolled out. AWW will continue to provide ‘Take Home Ration’ and nutrition specific counselling to the mothers. Recording of weight and growth monitoring will be done as per guidelines. Underweight children will be identified and taken up for further management. </p>

                        <p>ASHAs will be provided additional incentive of Rs. 250 for completion of 5 home visits under HBYC for each young child (Rs. 50 per visit) as per the recommended schedule and additional commodities namely ORS packet and Iron Folic Acid syrup will be provided in the kit. </p>

                        <p>HBYC is being implemented in a phased manner. In F.Y 2019-20, HBYC will be implemented across 240 districts including 115 Aspirational Districts across all States/UTs except Lakshadweep, Goa and Puducherry and financial approval is given for trainings, ASHA incentives, printing and monitoring costs.  
                            As on date, a total of 28 National Resource Team members (NRTs) and 166 State trainers trained from 28 States and 3 UTs. Twenty four states have completed state level trainings (ToTs) of HBYC and 1016 district level trainers are trained covering 205 districts across the Country. Nine States namely, Jammu & Kashmir, Maharashtra, Madhya Pradesh, Manipur, Meghalaya, Mizoram, Nagaland, Punjab and Sikkim have started ASHAs, ASHA Facilitators and ANMs training under HBYC.</p>

